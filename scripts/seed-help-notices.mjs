// 실행: node scripts/seed-help-notices.mjs

import { createClient } from '@supabase/supabase-js'
import fs from 'node:fs'
import path from 'node:path'

const env = Object.fromEntries(
  fs.readFileSync(path.resolve('.env.local'), 'utf8')
    .split('\n').map(l => l.trim()).filter(l => l && !l.startsWith('#'))
    .map(l => { const i = l.indexOf('='); return [l.slice(0, i), l.slice(i + 1)] })
)

const SUPABASE_URL = env.NEXT_PUBLIC_SUPABASE_URL
const SERVICE_KEY  = env.SUPABASE_SERVICE_ROLE_KEY
if (!SUPABASE_URL || !SERVICE_KEY) { console.error('env 누락'); process.exit(1) }

const supabase = createClient(SUPABASE_URL, SERVICE_KEY, { auth: { persistSession: false } })

// 1) 앱관리자(작성자) 결정
const { data: admins } = await supabase
  .from('cg_profiles')
  .select('id, full_name, created_at')
  .eq('status', 'active')
  .or('is_super_admin.eq.true,role.eq.admin')
  .order('created_at', { ascending: true })
if (!admins?.length) { console.error('활성 앱관리자가 없습니다.'); process.exit(1) }
const author = admins[0]
console.log(`✓ 작성자: ${author.full_name}`)

// 2) 기존 가이드 공지만 삭제 (제목 prefix 매칭)
const PREFIXES = ['[실무자]', '[관리자]', '[공통]']
const { data: existing } = await supabase
  .from('cg_notices')
  .select('id, title')
  .or(PREFIXES.map(p => `title.like.${p}%`).join(','))
if (existing?.length) {
  const ids = existing.map(n => n.id)
  await supabase.from('cg_notice_attachments').delete().in('notice_id', ids)
  await supabase.from('cg_notices').delete().in('id', ids)
  console.log(`✓ 기존 가이드 공지 ${existing.length}건 삭제`)
} else {
  console.log('✓ 기존 가이드 공지 없음 — 신규 등록만 진행')
}

