import { createClient } from '@supabase/supabase-js'
import fs from 'node:fs'
import path from 'node:path'

const envPath = path.resolve('.env.local')
const env = Object.fromEntries(
  fs.readFileSync(envPath, 'utf8')
    .split('\n').map(l => l.trim()).filter(l => l && !l.startsWith('#'))
    .map(l => { const i = l.indexOf('='); return [l.slice(0, i), l.slice(i + 1)] })
)

const SUPABASE_URL = env.NEXT_PUBLIC_SUPABASE_URL
const SERVICE_KEY = env.SUPABASE_SERVICE_ROLE_KEY
if (!SUPABASE_URL || !SERVICE_KEY) {
  console.error('env에서 Supabase URL / Service key를 찾지 못했습니다.')
  process.exit(1)
}

const supabase = createClient(SUPABASE_URL, SERVICE_KEY, {
  auth: { persistSession: false },
})

// 1) Admin 작성자 결정 (가장 먼저 만들어진 active admin)
const { data: admins, error: adminErr } = await supabase
  .from('cg_profiles')
  .select('id, full_name, email, created_at')
  .eq('role', 'admin')
  .eq('status', 'active')
  .order('created_at', { ascending: true })

if (adminErr) { console.error('admin 조회 실패:', adminErr); process.exit(1) }
if (!admins?.length) {
  console.error('활성 관리자 계정을 찾을 수 없습니다.')
  process.exit(1)
}
const author = admins[0]
console.log(`✓ 작성자: ${author.full_name} (${author.email})`)

// 2) 기존 공지 카운트 → 삭제
const { count: beforeCount } = await supabase
  .from('cg_notices')
  .select('id', { count: 'exact', head: true })
console.log(`✓ 기존 공지 ${beforeCount}건 → 삭제 진행`)

// 첨부파일 먼저 정리 (FK가 cascade가 아닐 수 있어 명시적으로)
await supabase.from('cg_notice_attachments').delete().not('id', 'is', null)
const { error: delErr } = await supabase
  .from('cg_notices')
  .delete()
  .not('id', 'is', null)
if (delErr) { console.error('공지 삭제 실패:', delErr); process.exit(1) }
console.log('✓ 기존 공지 전부 삭제됨')

